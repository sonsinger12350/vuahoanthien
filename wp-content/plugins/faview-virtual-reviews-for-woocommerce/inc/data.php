	private function __construct() {
		$this->init_params();
	}

	public static function instance() {
		// If the single instance hasn't been set, set it now.
		if ( null == self::$instance ) {
			self::$instance = new self();
		}

		return self::$instance;
	}

	public function init_params() {
		$option = get_option( 'wvr_data', [] );
		$params = wp_parse_args( $option, $this->default_data );

		if ( ! empty( $params['review_rules'] ) && is_array( $params['review_rules'] ) ) {
			$review_rules = [];
			foreach ( $params['review_rules'] as $rule_id ) {
				$review_rules[] = get_option( "wvr_cmt_rule_{$rule_id}" );
			}
			$params['review_rules'] = $review_rules;
		}

		$this->params = $params;
	}

	public function get_params() {
		if ( ! $this->params ) {
			$this->init_params();
		}

		return $this->params;
	}

	public function get_param( $key ) {
		if ( ! $this->params ) {
			$this->init_params();
		}

		return $this->params[ $key ] ?? '';
	}

